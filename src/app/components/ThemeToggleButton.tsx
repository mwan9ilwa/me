export default function ThemeToggleButton() {
  const { theme, setTheme, resolvedTheme } = useTheme();
  
  const cycleTheme = () => {
    if (theme === 'light') setTheme('dark');
    else if (theme === 'dark') setTheme('system');
    else setTheme('light');
  };

  if (!theme) return null; // Avoid rendering until theme is loaded

  return (
    <button onClick={cycleTheme} className={styles.toggleButton} aria-label={`Switch to ${
        theme === 'light' ? 'Dark' : theme === 'dark' ? 'System' : 'Light'
      } mode`}>
      {theme === 'system' ? <SystemIcon /> : resolvedTheme === 'light' ? <SunIcon /> : <MoonIcon />}
    </button>
  );
}